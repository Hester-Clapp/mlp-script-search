class Buffer {
    constructor(length, size) {
        this.buffer = new Array(size);
        this.head = 0;
        this.tail = 0;
        this.maxLength = length;
        this.size = size;
        this.length = 0;
    }

    add(item) {
        if (this.length === this.size) throw new Error("Queue full");
        if (this.length === this.maxLength) {
            this.head = (this.head + 1) % this.size; // Overwrite the oldest item
        } else {
            this.length++;
        }
        this.buffer[this.tail] = item;
        this.tail = (this.tail + 1) % this.size;
    }

    dequeue() {
        if (this.length === 0) return undefined;
        const item = this.buffer[this.head];
        this.head = (this.head + 1) % this.size;
        this.length--;
        return item;
    }

    get contents() {
        let output = []
        for (let i = this.head; i !== this.tail; i = (i + 1) % this.size) {
            output.push(this.buffer[i]);
        }
        return output;
    }
}

function getLineData(line) {
    let epCode = line.slice(0, 3)
    let season = parseInt(line.slice(0, 1))
    let episode = parseInt(line.slice(1, 3))
    let title = titles[line.slice(0, 3)]
    let speakerID = parseInt(line.slice(3, 6), 16)
    let speaker = speakers[parseInt(line.slice(3, 6), 16)]
    let text = line.slice(6)
    return {
        epCode,
        season,
        episode,
        title,
        speakerID,
        speaker,
        text,
        formatLine: () => {
            if (speakerID) {
                return `    ${speaker}: ${text}`
            } else {
                return `    ${text}`
            }
        },
        formatEpisode: () => {
            return `Season ${season}, Episode ${episode} - ${title}`
        }
    }
}

